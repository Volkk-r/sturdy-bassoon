// Примеры операторов в JavaScript:
let a = 5 + 3; // оператор сложения
let isEqual = a === 8; // оператор сравнения (строгое равенство)
let result = true && false; // логический оператор «И»

// Сложение (+)
let f = 5 + 3; // 8
// Конкатенация строк
let str1 = "Hello, " + "world!"; // "Hello, world!"
// Если один из операндов — строка, выполняется конкатенация (сложение строк).
let str2 = "The answer is: " + 42; // "The answer is: 42"

// Вычитание (-)
let j = 10 - 4; // 6

// Умножение (*)
let result1 = 6 * 7; // 42

// Деление (/)
let result2 = 20 / 5; // 4
// Если делить на ноль, результатом будет Infinity или -Infinity.
10 / 0; // Infinity
-10 / 0; // -Infinity

// Остаток от деления (%)
10 % 3; // 1

// Возведение в степень (**)
2 ** 3; // 8 (2 в степени 3)

// Скобки
// Чтобы изменить порядок вычислений, используют круглые скобки ().
let result3 = (2 + 3) * 4; // 20
// В этом случае сначала выполняется выражение в скобках, потом умножение.

// Равенство (==)
// Оператор == проверяет, равны ли значения, но при этом автоматически преобразует типы.
5 == 5; // true
5 == "5"; // true (строка преобразована в число)
0 == false; // true
// Такое поведение может приводить к неожиданным результатам, поэтому рекомендуется использовать строгое равенство.

// Строгое равенство (===)
// Оператор === сравнивает значения и типы данных. Совпадение только по значению без совпадения типов даст результат false.
5 === 5; // true
5 === "5"; // false (число и строка — разные типы)
0 === false; // false (число и логическое значение разные)

// В современном JavaScript принято использовать именно ===, чтобы избежать скрытых преобразований.

// Неравенство (!=)
5 != 3; // true
5 != "5"; // false (строка преобразована в число)

// Строгое неравенство (!==)
5 !== 3; // true
5 !== "5"; // true (значение то же, но типы разные)

// Больше и меньше (>, <)
10 > 5; // true

// Больше или равно / меньше или равно (>=, <=)
// Эти операторы проверяют не только больше/меньше, но и равенство.
5 >= 5; // true
3 <= 4; // true
// Всегда логический результат
// Все операторы сравнения возвращают только true или false. Это делает их основой для условий в конструкции if и других логических выражениях.
let tasks1 = 2;
console.log(tasks1 > 0); // true
console.log(tasks1 === 0); // false

// Логическое «И» — &&
// «И» даёт истину, только если истинны оба условия.
// Если левый операнд «ложный», правый даже не вычисляется — результатом будет левый.
true && true; // true
true && false; // false
"Анна" && 5; // 5  (оба truthy → возвращается последний)
"" && "текст"; // "" (пустая строка falsy → короткое замыкание)
0 && 10 / 0; // 0  (второй операнд не вычислится)
// Логическое «ИЛИ» — ||
// «ИЛИ» даёт истину, если истинно хотя бы одно из условий.
// Если левый операнд истинный, правый не вычисляется.
false || true; // true
"" || "без названия"; // "без названия" (первый falsy → берём следующий)
"текст" || 0; // "текст" (первый truthy → короткое замыкание)
null || undefined || "значение"; // "значение"
// Логическое «НЕ» — !
!true; // false
!false; // true

// Конструкция if
// Синтаксис:
// if (условие) {
//   // код, который выполняется только если условие истинно
// }

let tasks = 0;
if (tasks > 0) {
  console.log("Есть задачи для выполнения");
}

// Конструкция if ... else
// Иногда нужно выполнить одно действие, если условие истинно, и другое действие, если условие ложно. Для этого используется пара if ... else.
// Синтаксис:
// if (условие) {
//   // код для случая, когда условие истинно
// } else {
//   // код для случая, когда условие ложно
// }

if (tasks > 0) {
  console.log("Есть задачи для выполнения");
} else if (tasks === 0) {
  console.log("Список пуст, можно отдыхать");
}
