// Массивы — упорядоченные списки значений.
// Они позволяют хранить множество элементов под одним именем и обращаться к ним по порядковому номеру.
let tasks = ["Купить молоко", "Позвонить врачу", "Сделать уроки"];

// Объекты — коллекции свойств вида «ключ: значение».
// Они позволяют хранить данные с понятными названиями, а не только в строгом порядке.
let task = {
  id: 1,
  title: "Купить молоко",
  status: "активна",
};

// Синтаксис массива
// Массив задаётся с помощью квадратных скобок [ ]. Внутри скобок через запятую перечисляются элементы:
let numbers = [10, 20, 30, 40];
let fruits = ["яблоко", "банан", "груша"];
// numbers — массив из четырёх чисел.
// fruits — массив из трёх строк.


// Элементы массива могут быть разных типов: числа, строки, булевы значения, объекты, даже другие массивы.
let mixed = [42, "Привет", true, null, [1, 2, 3]];
// Однако на практике стараются хранить однотипные данные, чтобы массив имел логический смысл.
// Массив может быть пустым:
let empty = [];

// Индексация элементов
// Каждый элемент массива имеет порядковый номер — индекс. Индексация всегда начинается с нуля:
// первый элемент массива — индекс 0;
// второй — индекс 1;
// третий — индекс 2;
// последний элемент имеет индекс (длина массива – 1).
// Пример:
let colors = ["красный", "зелёный", "синий"];
console.log(colors[0]); // "красный"
console.log(colors[1]); // "зелёный"
console.log(colors[2]); // "синий"


// Доступ и изменение элементов по индексу
// Элемент массива можно получить, указав его индекс в квадратных скобках:
// имяМассива[индекс]
// Элементы массива можно не только читать, но и изменять:
let tasks1 = ["учёба", "спорт", "отдых"];
console.log(tasks1[1]); // "спорт"
tasks1[1] = "прогулка"; // изменили второй элемент
console.log(tasks1); // ["учёба", "прогулка", "отдых"]  

// Свойство .length
// Каждый массив имеет встроенное свойство .length, которое хранит количество элементов.
let numbers1  = [5, 10, 15, 20];
console.log(numbers1.length); // 4
// .length всегда возвращает число элементов массива, а не максимальный индекс.


// Примеры использования:
// Добавить элемент в конец массива:

let arr1 = [1, 2, 3];
let i = arr1.length; // i = 3
arr1[i] = 4;
console.log(arr1); // [1, 2, 3, 4]
// Получить последний элемент:

let arr2 = ["a", "b", "c"];
console.log(arr2[arr2.length - 1]); // "c"



// Синтаксис объекта
// Объект создаётся с помощью фигурных скобок { }.
// Внутри скобок перечисляются свойства — пары «ключ: значение», разделённые запятыми.
// Пример:
let user = {
  name: "Анна",
  age: 25,
  isAdmin: false
};


// Ключи объекта
// Ключ — это имя свойства. По умолчанию ключи являются строками (даже если записаны без кавычек).
// Примеры допустимых ключей:
let car = {
  brand: "Toyota",
  model: "Corolla",
  "year of issue": 2020
};

// Значения объекта
// Значением может быть любой тип данных:
// примитив (число, строка, логическое значение);
// массив;
// другой объект;
// функция.


// Пример:
let book = {
  title: "Война и мир",
  author: "Лев Толстой",
  year: 1869,
  genres: ["роман", "эпопея"],
  info: {
    pages: 1300,
    language: "русский"
  }
}

// Доступ к свойствам объекта
// Существует два основных способа получить значение свойства:
// Через точку (.)
console.log(user.name); // "Анна"
console.log(user.age);  // 25
// Синтаксис: имяОбъекта.ключ.
// Через квадратные скобки ([""])
console.log(user["name"]); // "Анна"
console.log(user["age"]);  // 25
// Синтаксис: имяОбъекта["ключ"].

let car1 = { "year of issue": 2020 };
console.log(car1["year of issue"]); // 2020
// Такой способ особенно полезен, если ключ содержит пробелы или специальные символы, либо если ключ хранится в переменной.


